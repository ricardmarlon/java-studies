public class AbreviandoOperadoresAritmetico {
    public static void main(String[] args) {
        int productQuantity = 10;

        // the same as in productQuantity = productQuantity + 3;
        productQuantity += 3;

        //
        productQuantity -= 3;

        //multiplication
        productQuantity *= 2;

        //division
        productQuantity /= 2;



        System.out.println(productQuantity);

    }
}
public class ExemploIntLong {
    public static void main(String[] args) {

        //over Java 7 you can use underscore to improve the reading
        int populacaoParaty = 43_680;

        System.out.println(populacaoParaty);

        long populacaoParaty2 = 2_147_483_648L;
        System.out.println(populacaoParaty2);
    }
}
